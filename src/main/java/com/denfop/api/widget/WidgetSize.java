package com.denfop.api.widget;

public class WidgetSize {

    private final int width;
    private final int height;

    public WidgetSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

}
