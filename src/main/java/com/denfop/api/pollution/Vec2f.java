package com.denfop.api.pollution;

public class Vec2f {

    public final int x;
    public final int y;

    public Vec2f(float xIn, float yIn) {
        this.x = (int) xIn;
        this.y = (int) yIn;
    }
    public Vec2f(int xIn, int yIn) {
        this.x = (int) xIn;
        this.y = (int) yIn;
    }
}
